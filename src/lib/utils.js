export function exportToCSV(data, filename) {
  const headers = Object.keys(data[0]);
  const csv = [
    headers.join(','),
    ...data.map(row => headers.map(h => row[h]).join(','))
  ].join('\n');

  const blob = new Blob([csv], { type: 'text/csv' });
  const url = window.URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  a.click();
  window.URL.revokeObjectURL(url);
}

export function exportToExcel(data, metadata, filename) {
  // Fallback ke CSV dengan format Excel-friendly jika library xlsx tidak tersedia
  const lines = [];
  
  // Header dengan informasi lengkap
  lines.push('LAPORAN DATA TIMBANG AYAM');
  lines.push('Smart Farm Layer 4.0');
  lines.push('');
  
  // Informasi Kandang
  lines.push('INFORMASI KANDANG');
  lines.push(`Kode Kandang,${metadata.kandangKode}`);
  lines.push(`Nama Kandang,${metadata.kandangNama}`);
  lines.push(`Penanggung Jawab,${metadata.penanggungJawab}`);
  lines.push(`Kontak,${metadata.kontak}`);
  if (metadata.kapasitas) {
    lines.push(`Kapasitas,${metadata.kapasitas} ekor`);
  }
  lines.push('');
  
  // Informasi Timbang
  lines.push('INFORMASI TIMBANG');
  lines.push(`Tanggal Export,${new Date().toLocaleDateString('id-ID')}`);
  
  // Cek tipe data
  if (metadata.umurMg) {
    // Single session data
    lines.push(`Umur Ayam,Minggu ${metadata.umurMg}`);
    lines.push(`Tanggal Timbang,${metadata.tanggal}`);
    lines.push(`Total Ekor Ditimbang,${metadata.totalEkor} ekor`);
    lines.push('');
    lines.push('HASIL ANALISA');
    lines.push(`Rata-rata Berat,${metadata.mean} gram`);
    lines.push(`Keseragaman (Uniformity),${metadata.uniformity}%`);
    lines.push(`Koefisien Variasi (CV),${metadata.cv}%`);
    lines.push(`Standar Deviasi,${metadata.stdDev} gram`);
    lines.push('');
    lines.push('DATA TIMBANG PER EKOR');
    lines.push('No,ID Ayam,Berat (gram),Waktu Input');
    
    data.forEach((row, index) => {
      lines.push(`${index + 1},${row.id_ayam},${row.berat},"${new Date(row.created_at).toLocaleString('id-ID')}"`);
    });
  } else {
    // Trend data
    lines.push(`Total Minggu,${metadata.totalMinggu}`);
    lines.push(`Total Ekor Keseluruhan,${metadata.totalEkor} ekor`);
    lines.push(`Rata-rata Keseluruhan,${metadata.rataRataKeseluruhan} gram`);
    lines.push('');
    lines.push('TREN PERTUMBUHAN PER MINGGU');
    lines.push('Minggu,Rata-rata Berat (gr),Total Ekor,Keseragaman (%),Tanggal Timbang');
    
    data.forEach((row) => {
      lines.push(`Minggu ${row.minggu},${row.rataRata},${row.totalEkor},${row.uniformity},"${row.tanggal}"`);
    });
  }
  
  // Footer
  lines.push('');
  lines.push(`Diekspor pada,"${new Date().toLocaleString('id-ID')}"`);
  lines.push('Generated by Smart Farm Layer 4.0 - shakadigital.com');
  
  // Buat file CSV yang Excel-friendly
  const csv = lines.join('\n');
  const blob = new Blob(['\ufeff' + csv], { type: 'text/csv;charset=utf-8;' });
  const url = window.URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename.replace('.xlsx', '.csv');
  a.click();
  window.URL.revokeObjectURL(url);
}

