export function getDistribution(weights, binSize = 100) {
  if (weights.length === 0) return [];
  
  const min = Math.min(...weights);
  const max = Math.max(...weights);
  const bins = [];
  
  for (let i = Math.floor(min / binSize) * binSize; i <= max; i += binSize) {
    const count = weights.filter(w => w >= i && w < i + binSize).length;
    bins.push({
      range: `${i}-${i + binSize}`,
      count
    });
  }
  
  return bins;
}
